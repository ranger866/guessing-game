from . import db
from flask_login import UserMixin

class User(db.Model, UserMixin):
    id = db.Column(db.Integer, primary_key=True)
    email = db.Column(db.String(150), unique=True, nullable=False)
    password = db.Column(db.String(150), nullable=False)
    first_name = db.Column(db.String(150), nullable=False)
    points = db.relationship('Point', backref='user', lazy=True)  # Menghubungkan dengan model Point

class Point(db.Model):  # db.Model harus huruf besar
    id = db.Column(db.Integer, primary_key=True)
    user_id = db.Column(db.Integer, db.ForeignKey('user.id'), nullable=False)
    points = db.Column(db.Integer, default=0, nullable=False)  # Inisialisasi poin dengan nilai 0
